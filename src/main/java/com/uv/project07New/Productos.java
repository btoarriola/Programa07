    private void btoGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btoGuardarActionPerformed
        if(isNew){
            Producto p=new Producto();
            p.setNombre(txtNombre.getText());
            double precio=Double.parseDouble(txtPrecio.getText());
            p.setPrecio(precio);
            daoProducto.create(p);
            txtClave.setText(String.valueOf(p.getClave()));
            JOptionPane.showMessageDialog(this, "se guardo");
        }
    }//GEN-LAST:event_btoGuardarActionPerformed

    private void btModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btModificarActionPerformed
        long id;
        id = Long.parseLong(txtClave.getText());
        Producto p=daoProducto.FindById(id);
        if(p!=null){
            Producto newp = new Producto();
            newp.setClave(p.getClave());
            newp.setNombre(txtNombre.getText());
            newp.setPrecio(Double.parseDouble(txtPrecio.getText()));
            daoProducto.update(newp, id);
            JOptionPane.showMessageDialog(this, "se actualizo producto");
        }else{
            JOptionPane.showMessageDialog(this,"No existe del pLong.valueOf(txtClave.getText())roducto");
        }
    }//GEN-LAST:event_btModificarActionPerformed

    private void btEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarActionPerformed
        Producto p=daoProducto.FindById(Long.parseLong(txtClave.getText()));
        if(p!=null){
            daoProducto.delete(Long.parseLong(txtClave.getText()));
            JOptionPane.showMessageDialog(this, "se elimino producto");
        }else{
            JOptionPane.showMessageDialog(this,"No existe del producto");
        }
    }//GEN-LAST:event_btEliminarActionPerformed
   
