    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        Producto p=daoProducto.FindById(Long.parseLong(txtClave.getText()));
        if(p!=null){
            txtNombre.setText(p.getNombre());
            txtPrecio.setText(String.valueOf(p.getPrecio()));
        }else{
            JOptionPane.showMessageDialog(this,"No existe del producto");
        }
    }//GEN-LAST:event_btBuscarActionPerformed

    public void fillData(){
        List<String> columns = new ArrayList<String>();
        List<Object[]> values = new ArrayList<Object[]>();
        
        columns.add("Clave");
        columns.add("Nombre");
        columns.add("Precio");
        List<Producto> lsProd = daoProducto.findAll();
        for(Producto producto : lsProd){
            Object[] row = new Object[]{String.valueOf(producto.getClave()), String.valueOf(producto.getNombre()), String.valueOf(producto.getPrecio())};
            values.add(row);
        }
        TableModel tablemodel= new DefaultTableModel(values.toArray(new Object[][] {}), columns.toArray());
        jTable1.setModel(tablemodel);
    }
